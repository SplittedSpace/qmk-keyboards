bool oled_task_user(void) {
    if (is_keyboard_master()) {
        render_l_logo();
        oled_set_cursor(3, 5);
        print_status_l_narrow();
    } else {
        render_r_logo();
        oled_set_cursor(3, 5);
        print_status_r_narrow();
    }
    // oled_set_brightness(255);
    return false;
}

