static void print_status_l_narrow(void) {
    // Print current modefs     
    oled_write_P(PSTR("\n\n"), false);

    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_ln_P(PSTR("ABC"), false);
            break;
        case 1:
            oled_write_ln_P(PSTR("123"), false);
            break;
        case 2:
            oled_write_ln_P(PSTR("< >"), false);
            break;
        case 3:
            oled_write_ln_P(PSTR("*"), false);
            break;
        default:
            oled_write_P(PSTR("Mod\n"), false);
            break;
    }

}

static void print_status_r_narrow(void) {
    oled_write_P(PSTR("\n"), false);
    led_t led_usb_state = host_keyboard_led_state();
    if (led_usb_state.caps_lock) {
        oled_write_P(PSTR("\n"), false);
        oled_write_ln_P(PSTR("CPSLK"), 0);
    }
    if (led_usb_state.num_lock) {
        oled_write_P(PSTR("\n"), false);
        oled_write_ln_P(PSTR("NLK"), 0);
    }
    if (led_usb_state.scroll_lock) {
        oled_write_P(PSTR("\n"), false);
        oled_write_ln_P(PSTR("SCRLK"), 0);
    }
}

