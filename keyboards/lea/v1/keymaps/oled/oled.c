int scale(int x, int in_min, int in_max, int out_min, int out_max)
{
  return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
}


void oled_draw_thin_raw_P(const char *data, uint16_t size, uint8_t width, uint8_t pos_col, uint8_t pos_row) {
    for (uint8_t j = 0; j < size/width; j++) {
        oled_set_cursor(pos_col, pos_row);
        
        char r[width];

        for (uint8_t i = 0; i < width; i++) {
            r[i] = (char)pgm_read_byte(&(data[j*width+i]));
            // oled_write_char((char)pgm_read_byte(&(data[j*width+i])), false);
        }

        oled_write_raw(r, sizeof(r));

        pos_row++;
    }
}

static void print_mode(uint8_t pos_col, uint8_t pos_row) {
    switch (get_highest_layer(layer_state)) {
        // Home
        case 0:
            oled_draw_thin_raw_P(mode_home, sizeof(mode_home), 24, pos_col, pos_row);
            break;
        
        // Raise
        case 1:
            oled_draw_thin_raw_P(mode_raise, sizeof(mode_raise), 24, pos_col, pos_row);
            break;

        // Lower
        case 2:
            oled_draw_thin_raw_P(mode_lower, sizeof(mode_lower), 24, pos_col, pos_row);
            break;

        // Star
        case 3:
            oled_draw_thin_raw_P(mode_star, sizeof(mode_star), 24, pos_col, pos_row);
            break;

        default:
            oled_write_P(PSTR("Mod\n"), false);
            break;
    }
}

static void print_rgb_status(void) {
    int val = 0;
    char str[2];

    // Mode 

    oled_set_cursor(0, 11);
    oled_write_raw_P(icon_rgb_mode, sizeof(icon_rgb_mode));
    oled_set_cursor(8, 10);

    #ifdef RGB_MATRIX_ENABLE
        val = rgb_matrix_get_mode();
    #endif

    #ifdef RGBLIGHT_ENABLE
        val = rgblight_get_mode();
    #endif
    
    sprintf(str,"%d", val);
    oled_write_ln(str, 0);


    // Speed

    oled_set_cursor(0, 13);
    oled_write_raw_P(icon_rgb_speed, sizeof(icon_rgb_speed));
    oled_set_cursor(8, 12);
    
    #ifdef RGB_MATRIX_ENABLE
        val = scale(rgb_matrix_get_speed(), 0, 255, 0, 99);
    #endif

    #ifdef RGBLIGHT_ENABLE
        val = scale(rgblight_get_speed(), 0, 255, 0, 99);
    #endif
    
    sprintf(str,"%d", val);
    oled_write_ln(str, 0);

    // Brightness

    oled_set_cursor(0, 15);
    oled_write_raw_P(icon_brightness1, sizeof(icon_brightness1));
    oled_set_cursor(8, 14);
    
    #ifdef RGB_MATRIX_ENABLE
        val = scale(rgb_matrix_get_val(), 0, RGB_MATRIX_MAXIMUM_BRIGHTNESS, 0, 99);
    #endif

    #ifdef RGBLIGHT_ENABLE
        val = scale(rgblight_get_val(), 0, RGBLIGHT_LIMIT_VAL, 0, 99);
    #endif
    
    sprintf(str,"%d", val);
    oled_write_ln(str, 0);

}

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    oled_set_brightness(255);
    return OLED_ROTATION_270;
}

bool oled_task_user(void) {

    // RGB status
    bool rgb_enabled = false;
    #ifdef RGB_MATRIX_ENABLE
        rgb_enabled = rgb_matrix_is_enabled();
    #endif
    #ifdef RGBLIGHT_ENABLE
        rgb_enabled = rgblight_is_enabled();
    #endif

    if (is_keyboard_master()) {
        print_mode(1, 3);
        if (rgb_enabled) {    
            print_rgb_status();
        }
    } else {
        oled_set_cursor(0, 3);
        render_logo();
    }
    return false;
}