int scale(int x, int in_min, int in_max, int out_min, int out_max)
{
  return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
}


void oled_draw_thin_raw_P(const char *data, uint16_t size, uint8_t width, uint8_t pos_col, uint8_t pos_row) {
    for (uint8_t j = 0; j < size/width; j++) {
        oled_set_cursor(pos_col, pos_row);
        
        char r[width];

        for (uint8_t i = 0; i < width; i++) {
            r[i] = (char)pgm_read_byte(&(data[j*width+i]));
            // oled_write_char((char)pgm_read_byte(&(data[j*width+i])), false);
        }

        oled_write_raw(r, sizeof(r));

        pos_row++;
    }
}

static void print_mode(uint8_t pos_col, uint8_t pos_row) {
    switch (get_highest_layer(layer_state)) {
        // Home
        case 0:
            oled_draw_thin_raw_P(mode_home, sizeof(mode_home), 24, pos_col, pos_row);
            break;
        
        // Raise
        case 1:
            oled_draw_thin_raw_P(mode_raise, sizeof(mode_raise), 24, pos_col, pos_row);
            break;

        // Lower
        case 2:
            oled_draw_thin_raw_P(mode_lower, sizeof(mode_lower), 24, pos_col, pos_row);
            break;

        // Star
        case 3:
            oled_draw_thin_raw_P(mode_star, sizeof(mode_star), 24, pos_col, pos_row);
            break;

        default:
            oled_write_P(PSTR("Mod\n"), false);
            break;
    }
}

static void print_light_status(void) {

    oled_set_cursor(0, 14);
    oled_write_raw_P(icon_brightness1, sizeof(icon_brightness1));
    oled_set_cursor(8, 13);


    int val = 0;
    
    #ifdef RGB_MATRIX_ENABLE
        val = scale(rgb_matrix_get_val(), 0, RGBLIGHT_LIMIT_VAL, 0, 99);
    #endif

    #ifdef RGBLIGHT_ENABLE
        val = scale(rgblight_get_val(), 0, RGBLIGHT_LIMIT_VAL, 0, 99);
    #endif
    
    char str[2];  
    sprintf(str,"%d", val);
    oled_write_ln(str, 0);
}

